class Product {
  final String name;
  final String brand;
  final String image;
  final String description;
  final double price;
  final String category;
  final String model;

  Product({
    required this.name,
    required this.brand,
    required this.description,
    required this.price,
    required this.category,
    required this.image,
    required this.model,
  });
}